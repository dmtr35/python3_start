# Основные функции из sys, которые тебе пригодятся:
import sys

# Функция	Что делает
print("Аргументы командной строки:", sys.argv)             # Список аргументов, переданных при запуске скрипта.
# Пример: python script.py папка1 папка2 → ['script.py', 'папка1', 'папка2']

print("Путь к исполняемому файлу Python:", sys.executable)  # Путь к исполняемому файлу Python.

print("Платформа (ОС):", sys.platform)                     # Название ОС: 'linux', 'win32', 'darwin' (macOS).

print("Версия Python:", sys.version)                       # Полная строка с версией Python.
print("Версия как структура:", sys.version_info)           # Версия в виде кортежа: (3, 12, 0, 'final', 0)

print("Пути поиска модулей:", sys.path)                    # Список путей, где Python ищет модули.
# sys.path.append("путь")                                  # Добавить свой путь для импорта модулей.

sys.stdout.write("stdout: обычный вывод\n")                # Вывод в стандартный поток (как print).
sys.stderr.write("stderr: сообщение об ошибке\n")          # Вывод в поток ошибок.

s = "hello"
print("Размер объекта в памяти:", sys.getsizeof(s), "байт") # Размер объекта в байтах.

